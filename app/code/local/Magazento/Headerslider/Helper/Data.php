class Magazento_Headerslider_Helper_Data extends Mage_Core_Helper_Abstract {

    public function versionUseAdminTitle() {
        $info = explode('.', Mage::getVersion());
        if ($info[0] > 1) {
            return true;
        }
        if ($info[1] > 3) {
            return true;
        }
        return false;
    }

    public function versionUseWysiwig() {
        $info = explode('.', Mage::getVersion());
        if ($info[0] > 1) {
            return true;
        }
        if ($info[1] > 3) {
            return true;
        }
        return false;
    }
    
    public function getImageFileHttp() {
        return Mage::getBaseUrl('media') . 'magazento_headerslider';
    }
    public function getImageFilePath() {
        return Mage::getBaseDir('media') . DS. 'magazento_headerslider';
    }    
    
}
